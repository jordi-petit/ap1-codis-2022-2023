/* Suma de dos enters */

#include <iostream>
using namespace std;

int main()
{
    cout << "escriu dos números: " << endl;
    int a, b; // declaració de variables
    cin >> a >> b; // lectura
    int s = a + b; // declaració amb inicialització
    cout << a << " + " << b << " = " << s << endl; // escriptura
}